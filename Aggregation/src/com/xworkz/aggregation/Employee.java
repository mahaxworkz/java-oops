package com.xworkz.aggregation;

public class Employee {
	
	int empid;
	String empname;
	String worktype;
	int workinghour;
	int noofshifts;
	
	public Employee(int empid,String empname,String worktype,int workinghour,int noofshifts) {
		this.empid= empid;
		this.empname= empname;
		this.worktype= worktype;
		this.workinghour= workinghour;
		this.noofshifts= noofshifts;
	}
	

}
